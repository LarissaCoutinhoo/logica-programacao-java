public class TiposPrimitivos{
    public static void main(String[] args){
        int ano = 2026;
        long habitantesPlaneta = 8142000000L;
        double meuSalario = 5000.00;
        char estadoAtividade = 'A';
        boolean devSucesso = true;

        System.out.println("Ano: " + ano);
        System.out.println("Número de habitantes no planeta: " + habitantesPlaneta);
        System.out.println("Meu salário: " + meuSalario);
        System.out.println("Meu estado de atividade: " + estadoAtividade);
        System.out.println("Sou uma dev de sucesso: " + devSucesso);
    }
}
// para atribuir valor a uma variável do tipo long, usamos obrigatóriamente o sufixo L
// para atribuir valor a uma varíavel do tipo char, usamos aspas simples